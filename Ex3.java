//Algoritmo utilizando WHILE. COontagem regressiva, começando em 100 e mostrando FIM ao final.

public class Ex3 {
	public static void main(String[] args) {
		int num = 10;
		while (num > -1) {
			System.out.println(num--); }
			//num = num - 1; ---forma alternativa---
	
		System.out.println("\nFim");
}}

